    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
        // TODO add your handling code here:
        int index = jTableInventario.getSelectedRow();

        if (index != -1 && JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de eliminar este articulo? ", "ATENCION", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0) {
            int id = Integer.parseInt(jTableInventario.getValueAt(index, 0).toString());
            Conexion con = new Conexion();
            con.eliminarArticulo(id);
            actualizarTabla(con.llenarTabla());
        } else if (index == -1) {
            JOptionPane.showMessageDialog(null, "Selecciona un articulo", "Ha ocurrido un error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_EliminarActionPerformed

    private void btn_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditarActionPerformed
        // TODO add your handling code here:
        int index = jTableInventario.getSelectedRow();
        
        
        if(index != -1){
          AgregarArticulo nuevoArticulo = new AgregarArticulo();
          nuevoArticulo.setEstado(1);
          nuevoArticulo.setId(jTableInventario.getValueAt(index, 0).toString());
          nuevoArticulo.setNombre(jTableInventario.getValueAt(index, 1).toString());
          nuevoArticulo.setDescripcion(jTableInventario.getValueAt(index, 2).toString());
          nuevoArticulo.setCantidad(jTableInventario.getValueAt(index, 3).toString());
          nuevoArticulo.setPrecio(jTableInventario.getValueAt(index, 4).toString());
          nuevoArticulo.cargarDatos();
          nuevoArticulo.revisarEstado();
          nuevoArticulo.show();
          this.dispose();
        }else{
           JOptionPane.showMessageDialog(null, "Selecciona un articulo", "Ha ocurrido un error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_EditarActionPerformed

    private void jTextBusquedaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextBusquedaKeyTyped
        // Convierte las letras a mayusculas
        char caracter = evt.getKeyChar();
        if (Character.isLowerCase(caracter) == true) {
            evt.setKeyChar(Character.toUpperCase(caracter));
        }
    }//GEN-LAST:event_jTextBusquedaKeyTyped

